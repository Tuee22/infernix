demoConfigGeneratedBannerLine :: String
demoConfigGeneratedBannerLine = "{- Auto-generated by infernix substrate materialization -}"

demoConfigGeneratedBanner :: String
demoConfigGeneratedBanner = demoConfigGeneratedBannerLine <> "\n"

encodeSubstrateConfig :: DemoConfig -> LazyChar8.ByteString
encodeSubstrateConfig demoConfig =
  LazyChar8.pack (demoConfigGeneratedBanner <> renderSubstrateConfig demoConfig)

decodeSubstrateConfigFile :: FilePath -> IO DemoConfig
decodeSubstrateConfigFile filePath = do
  decodedValue <- try (Dhall.inputFile Dhall.auto filePath :: IO DhallDemoConfig)
  case decodedValue of
    Left err ->
      ioError
        (userError ("invalid generated substrate Dhall: " <> displayException (err :: SomeException)))
    Right rawConfig ->
      case demoConfigFromDhall rawConfig of
        Left message -> ioError (userError ("invalid generated substrate Dhall: " <> message))
        Right demoConfig -> pure demoConfig

resolveRuntimeModeFromSubstrateFile :: FilePath -> IO RuntimeMode
resolveRuntimeModeFromSubstrateFile filePath =
  configRuntimeMode <$> decodeSubstrateConfigFile filePath

data DhallDemoConfig = DhallDemoConfig
  { dhallConfigRuntimeMode :: Text,
    dhallEdgePort :: Int,
    dhallConfigMapName :: Text,
    dhallGeneratedPath :: Text,
    dhallMountedPath :: Text,
    dhallDemoUi :: Bool,
    dhallDaemonRole :: Text,
    dhallClusterDaemon :: DhallDaemonConfig,
    dhallHostDaemon :: Maybe DhallDaemonConfig,
    dhallConfigRequestTopics :: [Text],
    dhallConfigResultTopic :: Text,
    dhallEngines :: [DhallEngineBinding],
    dhallModels :: [DhallModelDescriptor]
  }
  deriving (Generic)

instance Dhall.FromDhall DhallDemoConfig where
  autoWith _ = Dhall.genericAutoWith dhallInterpretOptions

data DhallDaemonConfig = DhallDaemonConfig
  { dhallRole :: Text,
    dhallLocation :: Text,
    dhallDaemonRequestTopics :: [Text],
    dhallDaemonResultTopic :: Text,
    dhallHostBatchTopic :: Maybe Text,
    dhallPulsarConnectionMode :: Text
  }
  deriving (Generic)

instance Dhall.FromDhall DhallDaemonConfig where
  autoWith _ = Dhall.genericAutoWith dhallInterpretOptions

data DhallEngineBinding = DhallEngineBinding
  { dhallEngine :: Text,
    dhallAdapterId :: Text,
    dhallAdapterType :: Text,
    dhallAdapterLocator :: Text,
    dhallAdapterEntrypoint :: Text,
    dhallSetupEntrypoint :: Text,
    dhallProjectDirectory :: Text,
    dhallPythonNative :: Bool
  }
  deriving (Generic)

instance Dhall.FromDhall DhallEngineBinding where
  autoWith _ = Dhall.genericAutoWith dhallInterpretOptions

data DhallModelDescriptor = DhallModelDescriptor
  { dhallMatrixRowId :: Text,
    dhallModelId :: Text,
    dhallDisplayName :: Text,
