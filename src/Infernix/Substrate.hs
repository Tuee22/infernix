    -- key on the dhall wire side.
    dhallCoordinator :: DhallDaemonConfig,
    dhallEngineDaemon :: Maybe DhallDaemonConfig,
    dhallEngineDaemons :: [DhallDaemonConfig],
    dhallEnginePools :: [DhallEnginePool],
