modelDescriptorFromDhall :: DhallModelDescriptor -> Either String ModelDescriptor
modelDescriptorFromDhall rawModel = do
  runtimeModeValue <- parseEnum "model runtimeMode" parseRuntimeMode (dhallModelRuntimeMode rawModel)
  runtimeLaneValue <- parseEnum "runtimeLane" parseRuntimeLane (dhallRuntimeLane rawModel)
  requestShapeValue <- traverse requestFieldFromDhall (dhallRequestShape rawModel)
  pure
    ModelDescriptor
      { matrixRowId = dhallMatrixRowId rawModel,
        modelId = dhallModelId rawModel,
        displayName = dhallDisplayName rawModel,
        family = dhallFamily rawModel,
        description = dhallDescription rawModel,
        artifactType = dhallArtifactType rawModel,
        referenceModel = dhallReferenceModel rawModel,
        downloadUrl = dhallDownloadUrl rawModel,
        selectedEngine = dhallSelectedEngine rawModel,
        requestShape = requestShapeValue,
        runtimeMode = runtimeModeValue,
        runtimeLane = runtimeLaneValue,
        requiresGpu = dhallRequiresGpu rawModel,
        notes = dhallNotes rawModel
      }

requestFieldFromDhall :: DhallRequestField -> Either String RequestField
requestFieldFromDhall rawField = do
  fieldTypeValue <- parseEnum "fieldType" parseRequestFieldType (dhallFieldType rawField)
  pure
    RequestField
      { name = dhallName rawField,
        label = dhallLabel rawField,
        fieldType = fieldTypeValue
      }

parseEnum :: String -> (Text -> Maybe a) -> Text -> Either String a
parseEnum label parser rawValue =
  case parser rawValue of
    Just parsedValue -> Right parsedValue
    Nothing -> Left ("unsupported " <> label <> ": " <> Text.unpack rawValue)

renderSubstrateConfig :: DemoConfig -> String
renderSubstrateConfig demoConfig =
  unlines
    [ "{ runtimeMode = " <> dhallText (runtimeModeId (configRuntimeMode demoConfig)),
      ", edgePort = " <> dhallInteger (configEdgePort demoConfig),
      ", configMapName = " <> dhallText (configMapName demoConfig),
      ", generatedPath = " <> dhallFilePath (generatedPath demoConfig),
      ", mountedPath = " <> dhallFilePath (mountedPath demoConfig),
      ", demo_ui = " <> dhallBool (demoUiEnabled demoConfig),
      ", daemonRole = " <> dhallText (daemonRoleId (activeDaemonRole demoConfig)),
      ", coordinator = " <> renderDaemonConfig (coordinatorDaemon demoConfig),
      ", engine = " <> dhallOptional daemonConfigType renderDaemonConfig (listToMaybe (engineDaemons demoConfig)),
      ", engineDaemons = " <> dhallList daemonConfigType renderDaemonConfig (engineDaemons demoConfig),
      ", enginePools = " <> dhallList enginePoolType renderEnginePool (enginePools demoConfig),
      ", engineMembers = " <> dhallList engineMemberType renderEngineMember (engineMembers demoConfig),
      ", request_topics = " <> dhallList "Text" dhallText (requestTopics demoConfig),
      ", result_topic = " <> dhallText (resultTopic demoConfig),
      ", models_bucket = " <> dhallText (modelsBucket demoConfig),
      ", model_bootstrap_topic = " <> dhallText (modelBootstrapTopic demoConfig),
      ", engines = " <> dhallList engineBindingType renderEngineBinding (engines demoConfig),
      ", models = " <> dhallList modelDescriptorType renderModelDescriptor (models demoConfig),
      "}"
    ]

