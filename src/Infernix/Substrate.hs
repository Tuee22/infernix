instance Dhall.FromDhall DhallModelDescriptor where
  autoWith _ = Dhall.genericAutoWith dhallInterpretOptions

data DhallRequestField = DhallRequestField
  { dhallName :: Text,
    dhallLabel :: Text,
    dhallFieldType :: Text
  }
  deriving (Generic)

instance Dhall.FromDhall DhallRequestField where
  autoWith _ = Dhall.genericAutoWith dhallInterpretOptions

dhallInterpretOptions :: Dhall.InterpretOptions
dhallInterpretOptions =
  Dhall.defaultInterpretOptions {Dhall.fieldModifier = dhallFieldName}

dhallFieldName :: Text -> Text
dhallFieldName rawFieldName =
  case Text.stripPrefix "dhall" rawFieldName of
    Nothing -> rawFieldName
    Just "ConfigRuntimeMode" -> "runtimeMode"
    Just "ConfigRequestTopics" -> "request_topics"
    Just "ConfigResultTopic" -> "result_topic"
    Just "DaemonRequestTopics" -> "request_topics"
    Just "DaemonResultTopic" -> "result_topic"
    Just "ModelRuntimeMode" -> "runtimeMode"
    Just "DemoUi" -> "demo_ui"
    Just "RequestTopics" -> "request_topics"
    Just "ResultTopic" -> "result_topic"
    Just "HostBatchTopic" -> "host_batch_topic"
    Just suffix -> lowerInitial suffix

lowerInitial :: Text -> Text
lowerInitial value =
  case Text.uncons value of
    Nothing -> value
    Just (firstCharacter, rest) -> Text.cons (toLower firstCharacter) rest

demoConfigFromDhall :: DhallDemoConfig -> Either String DemoConfig
demoConfigFromDhall rawConfig = do
  runtimeModeValue <- parseEnum "runtimeMode" parseRuntimeMode (dhallConfigRuntimeMode rawConfig)
  activeDaemonRoleValue <- parseEnum "daemonRole" parseDaemonRole (dhallDaemonRole rawConfig)
  clusterDaemonValue <- daemonConfigFromDhall (dhallClusterDaemon rawConfig)
  hostDaemonValue <- traverse daemonConfigFromDhall (dhallHostDaemon rawConfig)
  engineValues <- traverse engineBindingFromDhall (dhallEngines rawConfig)
  modelValues <- traverse modelDescriptorFromDhall (dhallModels rawConfig)
  pure
    DemoConfig
      { configRuntimeMode = runtimeModeValue,
        configEdgePort = dhallEdgePort rawConfig,
        configMapName = dhallConfigMapName rawConfig,
        generatedPath = Text.unpack (dhallGeneratedPath rawConfig),
        mountedPath = Text.unpack (dhallMountedPath rawConfig),
        demoUiEnabled = dhallDemoUi rawConfig,
        activeDaemonRole = activeDaemonRoleValue,
        clusterDaemon = clusterDaemonValue,
        hostDaemon = hostDaemonValue,
        requestTopics = dhallConfigRequestTopics rawConfig,
        resultTopic = dhallConfigResultTopic rawConfig,
        engines = engineValues,
        models = modelValues
      }

daemonConfigFromDhall :: DhallDaemonConfig -> Either String DaemonConfig
daemonConfigFromDhall rawConfig = do
  roleValue <- parseEnum "daemon role" parseDaemonRole (dhallRole rawConfig)
  connectionModeValue <- parseEnum "pulsarConnectionMode" parsePulsarConnectionMode (dhallPulsarConnectionMode rawConfig)
  pure
    DaemonConfig
      { daemonConfigRole = roleValue,
        daemonConfigLocation = dhallLocation rawConfig,
        daemonConfigRequestTopics = dhallDaemonRequestTopics rawConfig,
        daemonConfigResultTopic = dhallDaemonResultTopic rawConfig,
        daemonConfigHostBatchTopic = dhallHostBatchTopic rawConfig,
        daemonConfigPulsarConnectionMode = connectionModeValue
      }

engineBindingFromDhall :: DhallEngineBinding -> Either String EngineBinding
engineBindingFromDhall rawBinding =
  pure
    EngineBinding
      { engineBindingName = dhallEngine rawBinding,
        engineBindingAdapterId = dhallAdapterId rawBinding,
        engineBindingAdapterType = dhallAdapterType rawBinding,
        engineBindingAdapterLocator = dhallAdapterLocator rawBinding,
        engineBindingAdapterEntrypoint = dhallAdapterEntrypoint rawBinding,
        engineBindingSetupEntrypoint = dhallSetupEntrypoint rawBinding,
        engineBindingProjectDirectory = Text.unpack (dhallProjectDirectory rawBinding),
        engineBindingPythonNative = dhallPythonNative rawBinding
      }

modelDescriptorFromDhall :: DhallModelDescriptor -> Either String ModelDescriptor
modelDescriptorFromDhall rawModel = do
  runtimeModeValue <- parseEnum "model runtimeMode" parseRuntimeMode (dhallModelRuntimeMode rawModel)
  runtimeLaneValue <- parseEnum "runtimeLane" parseRuntimeLane (dhallRuntimeLane rawModel)
  requestShapeValue <- traverse requestFieldFromDhall (dhallRequestShape rawModel)
  pure
    ModelDescriptor
      { matrixRowId = dhallMatrixRowId rawModel,
        modelId = dhallModelId rawModel,
        displayName = dhallDisplayName rawModel,
        family = dhallFamily rawModel,
        description = dhallDescription rawModel,
        artifactType = dhallArtifactType rawModel,
        referenceModel = dhallReferenceModel rawModel,
        downloadUrl = dhallDownloadUrl rawModel,
        selectedEngine = dhallSelectedEngine rawModel,
        requestShape = requestShapeValue,
        runtimeMode = runtimeModeValue,
        runtimeLane = runtimeLaneValue,
        requiresGpu = dhallRequiresGpu rawModel,
        notes = dhallNotes rawModel
      }

requestFieldFromDhall :: DhallRequestField -> Either String RequestField
requestFieldFromDhall rawField = do
  fieldTypeValue <- parseEnum "fieldType" parseRequestFieldType (dhallFieldType rawField)
  pure
    RequestField
      { name = dhallName rawField,
        label = dhallLabel rawField,
        fieldType = fieldTypeValue
      }

parseEnum :: String -> (Text -> Maybe a) -> Text -> Either String a
parseEnum label parser rawValue =
  case parser rawValue of
    Just parsedValue -> Right parsedValue
    Nothing -> Left ("unsupported " <> label <> ": " <> Text.unpack rawValue)

renderSubstrateConfig :: DemoConfig -> String
renderSubstrateConfig demoConfig =
  unlines
    [ "{ runtimeMode = " <> dhallText (runtimeModeId (configRuntimeMode demoConfig)),
      ", edgePort = " <> dhallInteger (configEdgePort demoConfig),
      ", configMapName = " <> dhallText (configMapName demoConfig),
      ", generatedPath = " <> dhallFilePath (generatedPath demoConfig),
      ", mountedPath = " <> dhallFilePath (mountedPath demoConfig),
      ", demo_ui = " <> dhallBool (demoUiEnabled demoConfig),
      ", daemonRole = " <> dhallText (daemonRoleId (activeDaemonRole demoConfig)),
      ", clusterDaemon = " <> renderDaemonConfig (clusterDaemon demoConfig),
      ", hostDaemon = " <> dhallOptional daemonConfigType renderDaemonConfig (hostDaemon demoConfig),
      ", request_topics = " <> dhallList "Text" dhallText (requestTopics demoConfig),
      ", result_topic = " <> dhallText (resultTopic demoConfig),
      ", engines = " <> dhallList engineBindingType renderEngineBinding (engines demoConfig),
      ", models = " <> dhallList modelDescriptorType renderModelDescriptor (models demoConfig),
      "}"
    ]

renderDaemonConfig :: DaemonConfig -> String
renderDaemonConfig daemonConfig =
  "{ role = "
    <> dhallText (daemonRoleId (daemonConfigRole daemonConfig))
    <> ", location = "
    <> dhallText (daemonConfigLocation daemonConfig)
    <> ", request_topics = "
    <> dhallList "Text" dhallText (daemonConfigRequestTopics daemonConfig)
    <> ", result_topic = "
    <> dhallText (daemonConfigResultTopic daemonConfig)
    <> ", host_batch_topic = "
    <> dhallOptional "Text" dhallText (daemonConfigHostBatchTopic daemonConfig)
    <> ", pulsarConnectionMode = "
    <> dhallText (pulsarConnectionModeId (daemonConfigPulsarConnectionMode daemonConfig))
    <> " }"

renderEngineBinding :: EngineBinding -> String
renderEngineBinding engineBinding =
  "{ engine = "
    <> dhallText (engineBindingName engineBinding)
    <> ", adapterId = "
    <> dhallText (engineBindingAdapterId engineBinding)
    <> ", adapterType = "
    <> dhallText (engineBindingAdapterType engineBinding)
    <> ", adapterLocator = "
    <> dhallText (engineBindingAdapterLocator engineBinding)
    <> ", adapterEntrypoint = "
    <> dhallText (engineBindingAdapterEntrypoint engineBinding)
    <> ", setupEntrypoint = "
    <> dhallText (engineBindingSetupEntrypoint engineBinding)
    <> ", projectDirectory = "
    <> dhallFilePath (engineBindingProjectDirectory engineBinding)
    <> ", pythonNative = "
    <> dhallBool (engineBindingPythonNative engineBinding)
    <> " }"

