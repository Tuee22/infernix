renderRequestField :: RequestField -> String
renderRequestField requestField =
  "{ name = "
    <> dhallText (name requestField)
    <> ", label = "
    <> dhallText (label requestField)
    <> ", fieldType = "
    <> dhallText (requestFieldTypeId (fieldType requestField))
    <> " }"

dhallList :: String -> (a -> String) -> [a] -> String
dhallList itemType renderItem values =
  case values of
    [] -> "([] : List " <> itemType <> ")"
    _ -> "[ " <> intercalate ", " (map renderItem values) <> " ]"

dhallOptional :: String -> (a -> String) -> Maybe a -> String
dhallOptional itemType renderItem value =
  case value of
    Nothing -> "None " <> itemType
    Just item -> "Some " <> renderItem item

dhallBool :: Bool -> String
dhallBool value
  | value = "True"
  | otherwise = "False"

dhallInteger :: Int -> String
dhallInteger value
  | value < 0 = show value
  | otherwise = "+" <> show value

dhallFilePath :: FilePath -> String
dhallFilePath = dhallText . Text.pack

dhallText :: Text -> String
dhallText value =
  Text.unpack ("\"" <> DhallCore.escapeText value <> "\"")

daemonConfigType :: String
daemonConfigType =
  "{ role : Text, location : Text, request_topics : List Text, result_topic : Text, host_batch_topic : Optional Text, pulsarConnectionMode : Text }"

engineBindingType :: String
engineBindingType =
  "{ engine : Text, adapterId : Text, adapterType : Text, adapterLocator : Text, adapterEntrypoint : Text, setupEntrypoint : Text, projectDirectory : Text, pythonNative : Bool }"

requestFieldType :: String
requestFieldType =
  "{ name : Text, label : Text, fieldType : Text }"

modelDescriptorType :: String
modelDescriptorType =
  "{ matrixRowId : Text, modelId : Text, displayName : Text, family : Text, description : Text, artifactType : Text, referenceModel : Text, downloadUrl : Text, selectedEngine : Text, requestShape : List "
    <> requestFieldType
    <> ", runtimeMode : Text, runtimeLane : Text, requiresGpu : Bool, notes : Text }"
