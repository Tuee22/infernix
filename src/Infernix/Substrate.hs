dhallList :: String -> (a -> String) -> [a] -> String
dhallList itemType renderItem values =
  case values of
    [] -> "([] : List " <> itemType <> ")"
    _ -> "[ " <> intercalate ", " (map renderItem values) <> " ]"

dhallOptional :: String -> (a -> String) -> Maybe a -> String
dhallOptional itemType renderItem value =
  case value of
    Nothing -> "None " <> itemType
    Just item -> "Some " <> renderItem item

dhallBool :: Bool -> String
dhallBool value
  | value = "True"
  | otherwise = "False"

dhallInteger :: Int -> String
dhallInteger value
  | value < 0 = show value
  | otherwise = "+" <> show value

dhallFilePath :: FilePath -> String
dhallFilePath = dhallText . Text.pack

dhallText :: Text -> String
dhallText value =
  Text.unpack ("\"" <> DhallCore.escapeText value <> "\"")

